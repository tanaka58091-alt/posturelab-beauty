// 問題のキーから知識カードを取得
function getKnowledgeFor(problemKeys){
  const cards = [];
  const usedKeys = new Set();

  problemKeys.forEach(key => {
    if (KNOWLEDGE_BASE[key]) {
      KNOWLEDGE_BASE[key].forEach(card => {
        const id = key + '::' + card.title;
        if (!usedKeys.has(id)) {
          cards.push(card);
          usedKeys.add(id);
        }
      });
    }
  });

  // 必ずgeneralを2つ混ぜる
  KNOWLEDGE_BASE.general.slice(0,2).forEach(c => cards.push(c));

  // 最大6枚
  return cards.slice(0, 6);
}

export { KNOWLEDGE_BASE, getKnowledgeFor };
